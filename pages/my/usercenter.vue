<script>

var app = getApp();
export default {
data() {
return {
			opt:{},
			loading:false,
      isload: false,
			pageinfo: [],
			pagecontent: [],
			copyright:'',
			copyright_link:'',
			xixie:false,
			showxieyi:false,
			xycontent:'',
			thqrcode:'',
			showthqrcode:'',
			isapplymendian:0,
			mendiantel:'',
			pre_url:app.globalData.pre_url,
			hbmoney:0,
            hbtext:'',
            hb_logid:0
            ,closeImgError:false,
            popupTopErr:false,
            popupIconErr:false,
            popupBottomErr:false
        }
    },
	onShow:function() {
    if(app.globalData.platform=='wx' && app.globalData.hide_home_button==1){
      uni.hideHomeButton();
    }
	},
	onLoad: function (opt) {
		this.opt = app.getopts(opt);
		this.getdata(); 
	},
	onPullDownRefresh:function(e){
		this.getdata();
	},
	methods: {
		getdata:function(){
			var that = this;
			var id = 0;
			if (that.opt && that.opt.id) {
				id = that.opt.id;
			}
			that.loading = true;
			    app.get('ApiMy/usercenter',{id: id},function (data){
			        that.loading = false;
			        if(!data || typeof data !== 'object' || (typeof data.status !== 'undefined' && data.status == 0)){
			            app.post('ApiMy/getmemberinfo',{},function(res){
			                if(res && res.status==1 && res.data){
			                    var u2 = res.data || {};
			                    that.pageinfo = { bgcolor: '#ffffff', title: '会员中心' };
			                    that.pagecontent = [];
			                    that.mendiantel = u2.tel || '';
			                    uni.setNavigationBarTitle({ title: '会员中心' });
			                    that.loaded();
			                }
			            });
			            return;
			        }
			        var pageinfo = (data && data.pageinfo) ? data.pageinfo : {};
			        that.pageinfo = pageinfo;
			        that.pagecontent = (data && Array.isArray(data.pagecontent)) ? data.pagecontent : [];
			        that.copyright = (data && data.copyright) ? data.copyright : '';
			        that.copyright_link = (data && data.copyright_link) ? data.copyright_link : '';
			        that.thqrcode = (data && data.thqrcode) ? data.thqrcode : '';
			        that.showthqrcode = (data && data.showthqrcode) ? data.showthqrcode : '';
			        that.mendiantel = (data && data.mendiantel) ? data.mendiantel : '';
			        if(pageinfo && pageinfo.title){
			            uni.setNavigationBarTitle({ title: pageinfo.title });
			        }
                if(data && data.xixie){
                    that.xixie = data.xixie;
                }
                if(data && data.isapplymendian){
                    that.isapplymendian = data.isapplymendian;
                }
                var greenscore_hb = (data && data.greenscore_hb) ? data.greenscore_hb : {};
                console.log(greenscore_hb);
                that.hbmoney = greenscore_hb.hbmoney || 0;
                that.hbtext = greenscore_hb.hbtext || '';
                that.hb_logid = greenscore_hb.log_id || 0;
                if(greenscore_hb && greenscore_hb.show_hb){
                    that.$refs.popup.open('center');
                }
                that.loaded();
            });
			//获取升级协议
			app.get('ApiMy/getUpAgree',{'needinit':0},function (data){
				if(data && data.data && data.data.uplv_agree){
					that.showxieyi = true;
					that.xycontent = data.data.agree_content || '';
				}
			});
		},
		hidexieyi: function () {
			var that = this
		  //同意升级
		  app.get('ApiMy/agreeUplv',{'needinit':0},function (data){
		  	console.log(data);
		  	if(data.status==1){
				that.showxieyi = false;
		  		that.getdata();
		  	}
		  });
		},
		// 领取红包
		hbsuccess() {
			var that = this;
			console.log('点击领取');
			that.loading = true;
			app.post('ApiGreenScore/quitHb', {log_id:that.hb_logid}, function (res) {
				that.loading = false;
				app.alert(res.msg);
				that.$refs.popup.close();
				return;
				
			});
		},
		hbclose(){
			var that = this;
			console.log('点击关闭');
			that.loading = true;
			app.post('ApiGreenScore/closeHb', {log_id:that.hb_logid}, function (res) {
				that.loading = false;
				that.$refs.popup.close();
			});
		},
		callphone:function(e) {
			var phone = e.currentTarget.dataset.phone;
			uni.makePhoneCall({
				phoneNumber: phone,
				fail: function () {
				}
			});
		},
	}
}
</script>
